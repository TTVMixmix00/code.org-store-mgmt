package com.jayagra.type_conversion;

public class ToString {
    public static java.lang.String fromBoolean(final boolean b) {
        return java.lang.String.valueOf(b);
    }

    public static java.lang.String fromInteger(final int i) {
        return java.lang.String.valueOf(i);
    }

    public static java.lang.String fromDouble(final double d) {
        return java.lang.String.valueOf(d);
    }
}
